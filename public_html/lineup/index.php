<div id="jeada" style="display: none; max-width: 700px; border-radius: 1rem">
	Егор Михайлович Васильев -Saas Ustar (также известный как Jeada) (родился 15 сентября 1988 г., с. Майя, Мегино-Кангаласский район, Якутская АССР) — российский якутский рэп-исполнитель, основатель музыкального движения саха-рэп (рэп на якутском языке), группы Danketsy, поэт, активист молодёжной политики по проблемам сельской молодёжи. Начиная с 2003 г., выпустил 5 сольных альбомов, 3 клипа и 2 концертных видеодиска, кроме того, в 2005 г. исполнил главную роль в фильме режиссёра Чапчикона Зверева «Далаһа устун уҥуор хаамабын» («Берега надежды»)[1]. В своё время стал самым молодым исполнителем, ставшим известным в Республике Саха (Якутия) и некоторых других регионах России (дебютный трек «Мин сахабын» («Я — якут») он записал в 9 классе)[2]. В настоящее время является студентом Российского государственного университета им. А.И. Герцена. Женат с 2007 г., в 2011 г. родился сын. Летом 2012 г. получил звание «Отличника молодёжной политики Республики Саха (Якутия)».
</div>
